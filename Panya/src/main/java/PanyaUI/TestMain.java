    public void initTheme(Color primary, Color light, Color dark){
        if (primary == null || light == null || dark == null){
            return;
        }

        final var PRIMARY = primary;
        final var LIGHT = light;
        final var DARK = dark;

        final var PRIMARY_TEXT = Theme.textColorFromBackgroundColor(PRIMARY);
        final var LIGHT_TEXT = Theme.textColorFromBackgroundColor(LIGHT);
        final var DARK_TEXT = Theme.textColorFromBackgroundColor(DARK);

        this.HIGHLIGHT_LABEL_COLOR = PRIMARY;
        this.HIGHLIGHT_TEXT_LABEL_COLOR = PRIMARY_TEXT;
        this.DEFAULT_LABEL_COLOR = LIGHT;
        this.DEFAULT_TEXT_LABEL_COLOR = LIGHT_TEXT;

        //MainPannel.setBackground(PRIMARY);
        Section.setBackground(LIGHT);
        
        HeaderContent.setBackground(PRIMARY);
        HeaderContentLabel.setForeground(PRIMARY_TEXT);

        //Header.setBackground(PRIMARY);

        HomeContent.setForeground(PRIMARY_TEXT);
        HomeLabel.setBackground(LIGHT);
        HomeLabel.setForeground(LIGHT_TEXT);

        MenuContent.setForeground(PRIMARY_TEXT);
        MenuLabel.setBackground(LIGHT);
        MenuLabel.setForeground(LIGHT_TEXT); 

        RecipeContent.setForeground(PRIMARY_TEXT);
        RecipeLabel.setBackground(LIGHT);
        RecipeLabel.setForeground(LIGHT_TEXT);

        SettingsContent.setForeground(PRIMARY_TEXT);
        SettingsLabel.setBackground(LIGHT); 
        SettingsLabel.setForeground(LIGHT_TEXT);
        
        StorageContent.setForeground(PRIMARY_TEXT);
        StorageLabel.setBackground(LIGHT);
        StorageLabel.setForeground(LIGHT_TEXT);

        Title.setBackground(DARK); 
        TitleLabel.setForeground(DARK_TEXT); 
    }

    public void initTheme(String themeName){
        var theme = new Theme().getTheme(themeName);
        if (theme == null){
            return;
        }

        final var PRIMARY = theme.get(Theme.PRIMARY);
        final var LIGHT = theme.get(Theme.LIGHT);
        final var DARK = theme.get(Theme.DARK);
    
        this.initTheme(PRIMARY, LIGHT, DARK);
    }

