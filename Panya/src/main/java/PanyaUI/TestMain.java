    private void highlightLabelColor(JLabel lbl){
        if (PRIDE){
            initTheme(Theme.getRandomThemeName());
        }
        lbl.setBackground(HIGHLIGHT_LABEL_COLOR);
        lbl.setForeground(HIGHLIGHT_TEXT_LABEL_COLOR);
    }
    private void defaultLabelColor(JLabel lbl){
        lbl.setBackground(DEFAULT_LABEL_COLOR);
        lbl.setForeground(DEFAULT_TEXT_LABEL_COLOR);
    }

